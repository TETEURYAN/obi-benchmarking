"""
Domain entity for user implementation (Etapa 4) and test results (Etapa 5).

Stores LLM code, user code, feedback and final test status.
"""


class UserImplementation:
    """
    Record for Etapa 4: LLM code, user code, and analysis feedback.

    Attributes:
        id_questao: Problem id.
        id_usuario: User id.
        codigo_llm: Solution code generated by the LLM.
        codigo_user: Code submitted by the user.
        feedback: Analysis agent feedback (or empty if ok).
    """

    __slots__ = ("_id_questao", "_id_usuario", "_codigo_llm", "_codigo_user", "_feedback")

    def __init__(
        self,
        id_questao: str,
        id_usuario: str,
        codigo_llm: str,
        codigo_user: str,
        feedback: str = "",
    ) -> None:
        self._id_questao = id_questao
        self._id_usuario = id_usuario
        self._codigo_llm = codigo_llm
        self._codigo_user = codigo_user
        self._feedback = feedback

    @property
    def id_questao(self) -> str:
        return self._id_questao

    @property
    def id_usuario(self) -> str:
        return self._id_usuario

    @property
    def codigo_llm(self) -> str:
        return self._codigo_llm

    @property
    def codigo_user(self) -> str:
        return self._codigo_user

    @property
    def feedback(self) -> str:
        return self._feedback


class UserTestResult:
    """
    Record for Etapa 5: test run result (pass/fail, feedback without inputs).

    Attributes:
        id_questao: Problem id.
        id_usuario: User id.
        status: e.g. "passed" or "failed".
        feedback_inputs: Optional feedback (inputs not revealed to user).
    """

    __slots__ = ("_id_questao", "_id_usuario", "_status", "_feedback_inputs")

    def __init__(
        self,
        id_questao: str,
        id_usuario: str,
        status: str,
        feedback_inputs: str | None = None,
    ) -> None:
        self._id_questao = id_questao
        self._id_usuario = id_usuario
        self._status = status
        self._feedback_inputs = feedback_inputs

    @property
    def id_questao(self) -> str:
        return self._id_questao

    @property
    def id_usuario(self) -> str:
        return self._id_usuario

    @property
    def status(self) -> str:
        return self._status

    @property
    def feedback_inputs(self) -> str | None:
        return self._feedback_inputs
